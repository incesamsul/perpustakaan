@section('script')
<script>
    $(document).ready(function() {


        // TOMBOL EDIT USER
        $('.table-kategori tbody').on('click', 'tr td a.edit', function() {
            let edit = $(this).data('edit');
            $('#nama_kategori').val(edit.nama_kategori);
            $('#id').val(edit.id_kategori);
            $('#formKategori').attr('action','/admin/update_kategori')
        })

        // TOMBOL TAMBAH USER
        $('#addUserBtn').on('click', function() {
            $('#formKategori').attr('action','/admin/create_kategori')
        });

        // TOMBOL HAPUS USER
        $('.table-kategori tbody').on('click', 'tr td a.hapus', function() {
            let id = $(this).data('id');
            Swal.fire({
                title: 'Apakah yakin?'
                , text: "Data tidak bisa kembali lagi!"
                , type: 'warning'
                , showCancelButton: true
                , confirmButtonColor: '#3085d6'
                , cancelButtonColor: '#d33'
                , confirmButtonText: 'Ya, Konfirmasi'
            }).then((result) => {
                if (result.value) {
                    $.ajax({
                        headers: {
                            'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
                        }
                        , url: '/admin/delete_kategori'
                        , method: 'post'
                        , dataType: 'json'
                        , data: {
                            id: id
                        }
                        , success: function(data) {
                            if (data == 1) {
                                Swal.fire('Berhasil', 'Data telah terhapus', 'success').then((result) => {
                                    location.reload();
                                });
                            }
                        }
                    })
                }
            })
        });





    });

    $('#liKategori').addClass('aktif');

</script>
@endsection
